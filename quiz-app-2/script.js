const quizForm = document.getElementById("quiz-form");
const submitBtn = document.getElementById("submit-btn");
const retakeBtn = document.getElementById("retake-btn");

const resultContainer = document.getElementById("result");
let randomQuestions = getRandomQuestions(5); 

// Function to get random questions
function getRandomQuestions(numQuestions) {
  const shuffledQuestions = [...questions].sort(() => 0.5 - Math.random());
  return shuffledQuestions.slice(0, numQuestions);
}

// Function to create quiz questions
function createQuiz(questionsArray) {
  quizForm.innerHTML = ""; // Clear existing questions

  questionsArray.forEach((questionObj, index) => {
    const questionRow = document.createElement("div");
    questionRow.className = "question-row";

    const question = document.createElement("p");
    question.textContent = `${index + 1}: ${questionObj.question}`;

    const optionsDiv = document.createElement("div");
    optionsDiv.className = "options";
    optionsDiv.dataset.correct = questionObj.correctAnswer;

    questionObj.options.forEach((optionText) => {
      const label = document.createElement("label");
      const radioInput = document.createElement("input");
      radioInput.type = "radio";
      radioInput.name = `q${index + 1}`;
      radioInput.value = optionText;

      label.appendChild(radioInput);
      label.appendChild(document.createTextNode(optionText));

      optionsDiv.appendChild(label);
    });

    questionRow.appendChild(question);
    questionRow.appendChild(optionsDiv);

    quizForm.appendChild(questionRow);
  });
}

// Function to calculate score
function calculateScore() {
  const questionRows = document.querySelectorAll(".question-row");
  let totalQuestions = questionRows.length;
  let correctAnswers = 0;

  for (let i = 0; i < totalQuestions; i++) {
    const selectedOption = questionRows[i].querySelector("input:checked");
    if (selectedOption && selectedOption.value === randomQuestions[i].correctAnswer) {
      correctAnswers++;
    }
  }

  return correctAnswers;
}

// Function to show quiz result
